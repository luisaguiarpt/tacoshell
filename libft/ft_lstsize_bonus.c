#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*ptr;

	ptr = lst;
	i = 0;
	while (ptr != 0)
	{
		ptr = ptr->next;
		i++;
	}
	return (i);
}
